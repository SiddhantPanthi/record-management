    class Person{
        String name,mail_id,password,department,category;
        Person(String n,String m,String p,String d,String c)
        {
            name=n;
            mail_id=m;
            password=p;
            department=d;
            category=c;
        }
        public String toString()
        {
            return name+" "+mail_id+" "+password+" "+department+" "+category;
        }
        String getName()
        {
            return name;
        }
        String getPassword()
        {
            return password;
        }
        String getCategory()
        {
            return category;
        }
        
        }
    
